    selectInput(inputId="variable", 
                label=p("Elija la variable",
                        span("cualitativa", style = "color:red"),
                        "para realizar la prueba de hipótesis."),
                choices="placeholder1"),
    
    selectInput(inputId="niveles", 
                label=p("Elija un",
                        span("nivel", style = "color:blue"),
