library(shiny)
library(markdown)

shinyUI(pageWithSidebar(
  headerPanel(title=HTML("Prueba de hipótesis para la proporción P"),
              windowTitle="PH proporción"),
  sidebarPanel(
    h5('Esta aplicación realiza la prueba de hipótesis para la 
